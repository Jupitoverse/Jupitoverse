def add_companies():
    """Add rich company data"""
    init_db()
    db = SessionLocal()
    
    try:
        added = 0
        updated = 0
        
        for company_data in RICH_COMPANIES:
            existing = db.query(Company).filter(Company.name == company_data["name"]).first()
            
            if existing:
                # Update existing
                for key, value in company_data.items():
                    if value is not None:
                        setattr(existing, key, value)
                updated += 1
            else:
                # Add new
                company = Company(**company_data)
                db.add(company)
                added += 1
        
        db.commit()
        logger.info(f"‚úÖ Added {added} new companies, updated {updated} existing")
        logger.info(f"üìä Total companies now: {db.query(Company).count()}")
        
    except Exception as e:
        logger.error(f"‚ùå Error: {e}")
        db.rollback()
    finally:
        db.close()


if __name__ == "__main__":
    add_companies()

